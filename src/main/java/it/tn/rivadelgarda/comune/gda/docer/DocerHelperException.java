package it.tn.rivadelgarda.comune.gda.docer;

public class DocerHelperException extends Exception {

	public DocerHelperException() {
		super();
	}

	public DocerHelperException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DocerHelperException(String message, Throwable cause) {
		super(message, cause);
	}

	public DocerHelperException(String message) {
		super(message);
	}

	public DocerHelperException(Throwable cause) {
		super(cause);
	}

}
